void test( void );

/*----------------------------------------------------------------------------*/
/*                             External Functions                             */
/*----------------------------------------------------------------------------*/

int main( int argc, char *argv[] )
{
    CU_pSuite suite = NULL;

    if( CUE_SUCCESS == CU_initialize_registry() ) {
        suite = CU_add_suite( "ibus-radio-protocol Test", NULL, NULL );

        if( NULL != suite ) {
            CU_add_test( suite, "Test bcd_track_converter()", test );
            CU_basic_set_mode( CU_BRM_VERBOSE );
            CU_basic_run_tests();
            printf( "\n" );
            CU_basic_show_failures( CU_get_failure_list() );
            printf( "\n\n" );
        }

        CU_cleanup_registry();
    }

    return CU_get_error();
}

/*----------------------------------------------------------------------------*/
/*                             Internal functions                             */
/*----------------------------------------------------------------------------*/
void test( void )
{
    int i;
    for( i = 0; i < 256; i++ ) {
        CU_ASSERT_EQUAL( __map[i], bcd_track_converter(i) );
    }
}
